package devdojo;

public class EstruturasDeRepetição {
    public static void main(String[] args) {
        // while, do while e for
        int count = 9;
        while(count <= 10){
            System.out.println(count);
            count = count + 1;
        }
        count = 9;
        do {
            System.out.println(count);
            count = count +1;
        }while(count <= 10);




    }
}
